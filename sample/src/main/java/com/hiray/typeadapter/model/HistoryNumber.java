public class HistoryNumber {

    public boolean isEven;
    public String one;
    public String two;
    public String three;
    public String four;

    public HistoryNumber(boolean isEven, String one, String two, String three, String four) {
        this.isEven = isEven;
        this.one = one;
        this.two = two;
        this.three = three;
        this.four = four;
    }
}
